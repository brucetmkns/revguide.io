// Icon mappings for banner types
const BannerTypeIcons = {
  info: Icons.info,
  success: Icons.checkCircle,
  warning: Icons.alertTriangle,
  error: Icons.xCircle,
  'battle-card': Icons.target,
};

// Icon mappings for card types
const CardTypeIcons = {
  competitor: Icons.swords,
  objection: Icons.shield,
  tip: Icons.lightbulb,
  process: Icons.listOrdered,
};

// Helper to get icon HTML
function getIcon(name, size = 20) {
  const iconFn = Icons[name];
  if (!iconFn) return '';
  return iconFn().replace('width="24"', `width="${size}"`).replace('height="24"', `height="${size}"`);
}

// Helper to get banner type icon
function getBannerIcon(type, size = 18) {
  const iconFn = BannerTypeIcons[type] || BannerTypeIcons.info;
  return iconFn().replace('width="24"', `width="${size}"`).replace('height="24"', `height="${size}"`);
}

// Helper to get card type icon
function getCardIcon(type, size = 18) {
  const iconFn = CardTypeIcons[type] || CardTypeIcons.tip;
  return iconFn().replace('width="24"', `width="${size}"`).replace('height="24"', `height="${size}"`);
}

